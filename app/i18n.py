def get_lang():
    lang = session.get("lang", DEFAULT_LANG)
    return lang if lang in LANGUAGES else DEFAULT_LANG


def translate(key):
    lang = get_lang()
    table = TRANSLATIONS.get(lang, {})
    if key in table:
        return table[key]
    return TRANSLATIONS[DEFAULT_LANG].get(key, key)
